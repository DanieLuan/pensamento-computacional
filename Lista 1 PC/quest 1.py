a = float(input('Digite o número A: '))
b = float(input('Digite o número B: '))

soma = a+b
dif = a-b
div = a/b
divInt = a//b
resto = a%b

print(str(a) + ' + ' + str(b) + ' = ' + str(soma))
print(str(a) + ' - ' + str(b) + ' = ' + str(dif))
print(str(a) + ' / ' + str(b) + ' = ' + str(div))
print(str(a) + ' // ' + str(b) + ' = ' + str(divInt))
print(str(a) + ' % ' + str(b) + ' = ' + str(resto))